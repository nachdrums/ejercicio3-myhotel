package com.prueba.geeks.dto;

public class Ejercicio5DTO {

	public String departamento;
	
	public String promedioSalario;
	
	public long cantidadEmpleados;

	public String getDepartamento() {
		return departamento;
	}

	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}

	public String getPromedioSalario() {
		return promedioSalario;
	}

	public void setPromedioSalario(String promedioSalario) {
		this.promedioSalario = promedioSalario;
	}

	public long getCantidadEmpleados() {
		return cantidadEmpleados;
	}

	public void setCantidadEmpleados(long cantidadEmpleados) {
		this.cantidadEmpleados = cantidadEmpleados;
	}



}
